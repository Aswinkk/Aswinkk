package com.qspiders.calculatorapp;

public class Calculator {
	int add(int a, int b) {
		int res = a + b;
		return res;
	}

	int mult(int a, int b) {
		int res = a * b;
		return res;
	}

	int div(int a, int b) {
		int res = a / b;
		return res;
	}

	int cube(int a) {
		int res = a * a * a;
		return res;
	}

}
